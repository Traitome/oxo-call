/// LLM-based command generator.
///
/// This is the primary generator that uses LLM API to generate commands
/// from natural language descriptions.
#[allow(dead_code)]
pub struct LlmCommandGenerator {
    client: LlmClient,
}

#[allow(dead_code)]
impl LlmCommandGenerator {
    /// Creates a new LLM command generator.
    pub fn new(client: LlmClient) -> Self {
        Self { client }
    }
}

#[async_trait]
impl CommandGenerator for LlmCommandGenerator {
    async fn generate(
        &self,
        tool: &str,
        docs: &str,
        skill: Option<&Skill>,
        user_request: &str,
        _config: &Config,
    ) -> Result<GeneratedCommand> {
        // Use the existing LlmClient to generate command
        let suggestion = self
            .client
            .suggest_command(tool, docs, user_request, skill, false, None)
            .await?;

        // Convert LlmCommandSuggestion to GeneratedCommand
        Ok(GeneratedCommand {
            args: suggestion.args.join(" "),
            explanation: suggestion.explanation,
            confidence: 0.85, // LLM-generated commands have moderate-high confidence
            source: CommandSource::Llm {
                model: self.client.config.effective_model(),
                cache_hit: suggestion.inference_ms == 0.0,
            },
        })
    }

    fn name(&self) -> &str {
        "llm"
    }
}

