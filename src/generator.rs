/// Rule-based command generator for simple, deterministic commands.
///
/// This generator uses pattern matching to generate commands for
/// common operations without calling the LLM.
#[allow(dead_code)]
pub struct RuleCommandGenerator {
    rules: Vec<CommandRule>,
}

/// A single command generation rule.
#[derive(Debug, Clone)]
#[allow(dead_code)]
pub struct CommandRule {
    /// Rule identifier
    pub id: String,
    /// Tool pattern (e.g., "samtools", "fastqc")
    pub tool_pattern: String,
    /// Request patterns to match (e.g., "sort bam", "quality check")
    pub request_patterns: Vec<String>,
    /// Template for generating args (supports {input}, {output} placeholders)
    pub args_template: String,
    /// Explanation template
    pub explanation_template: String,
    /// Confidence when this rule matches
    pub confidence: f32,
}

#[allow(dead_code)]
impl RuleCommandGenerator {
    /// Creates a new rule-based generator with default rules.
    pub fn new() -> Self {
        Self {
            rules: Self::default_rules(),
        }
    }

    /// Creates a generator with custom rules.
    pub fn with_rules(rules: Vec<CommandRule>) -> Self {
        Self { rules }
    }

    /// Default rules for common operations.
    fn default_rules() -> Vec<CommandRule> {
        vec![
            // samtools rules
            CommandRule {
                id: "samtools_sort".to_string(),
                tool_pattern: "samtools".to_string(),
                request_patterns: vec!["sort bam".to_string(), "sort by coordinate".to_string()],
                args_template: "sort -o {output}.bam {input}.bam".to_string(),
                explanation_template: "Sort BAM file by genomic coordinates".to_string(),
                confidence: 0.95,
            },
            CommandRule {
                id: "samtools_index".to_string(),
                tool_pattern: "samtools".to_string(),
                request_patterns: vec!["index bam".to_string(), "create bai".to_string()],
                args_template: "index {input}.bam".to_string(),
                explanation_template: "Create BAM index file (.bai)".to_string(),
                confidence: 0.95,
            },
            CommandRule {
                id: "samtools_flagstat".to_string(),
                tool_pattern: "samtools".to_string(),
                request_patterns: vec!["flagstat".to_string(), "alignment statistics".to_string()],
                args_template: "flagstat {input}.bam".to_string(),
                explanation_template: "Show alignment statistics".to_string(),
                confidence: 0.95,
            },
            // fastqc rules
            CommandRule {
                id: "fastqc_basic".to_string(),
                tool_pattern: "fastqc".to_string(),
                request_patterns: vec!["quality check".to_string(), "qc".to_string()],
                args_template: "{input}.fastq.gz".to_string(),
                explanation_template: "Run quality control on FASTQ files".to_string(),
                confidence: 0.90,
            },
            // bwa rules
            CommandRule {
                id: "bwa_index".to_string(),
                tool_pattern: "bwa".to_string(),
                request_patterns: vec!["index".to_string(), "build index".to_string()],
                args_template: "index {input}.fa".to_string(),
                explanation_template: "Build BWA index for reference genome".to_string(),
                confidence: 0.95,
            },
        ]
    }

    /// Checks if a rule matches the given tool and request.
    fn matches(&self, rule: &CommandRule, tool: &str, request: &str) -> bool {
        // Check tool pattern (exact match for now)
        if rule.tool_pattern != tool {
            return false;
        }

        // Check request patterns (case-insensitive substring match)
        // Use contains_ignore_case to avoid lowercase allocations
        rule.request_patterns
            .iter()
            .any(|p| contains_ignore_case(request, p))
    }

    /// Applies a rule to generate a command.
    fn apply(&self, rule: &CommandRule, request: &str) -> GeneratedCommand {
        // Try to extract file names from the request using common patterns.
        // Use iterator directly without intermediate Vec allocation
        let mut input_name = "input".to_string();
        let mut output_name = "output".to_string();
        let mut prev_word: Option<&str> = None;

        for word in request.split_whitespace() {
            // Use eq_ignore_ascii_case and contains checks without lowercase allocation
            let has_dot = word.contains('.');
            let is_flag = word.starts_with('-') || word.starts_with("--");
            // Detect file-like tokens (containing a dot extension)
            if has_dot && !is_flag {
                if input_name == "input" {
                    input_name = word.trim_end_matches([',', ';']).to_string();
                } else if output_name == "output" {
                    output_name = word.trim_end_matches([',', ';']).to_string();
                }
            }
            // Detect "to <file>" or "output <file>" patterns (case-insensitive)
            // Check if previous word was a keyword
            if let Some(prev) = prev_word {
                let was_keyword = prev.eq_ignore_ascii_case("to")
                    || prev.eq_ignore_ascii_case("output")
                    || prev.eq_ignore_ascii_case("into");
                if was_keyword && word.contains('.') {
                    output_name = word.trim_end_matches([',', ';']).to_string();
                }
            }
            prev_word = Some(word);
        }

        // Strip common extensions for placeholder substitution
        let strip_extensions = |name: &str| -> String {
            let mut s = name.to_string();
            // Strip compound extensions like .fastq.gz, .vcf.gz first
            for compound in &[".fastq.gz", ".fq.gz", ".vcf.gz", ".bed.gz", ".fa.gz"] {
                if s.ends_with(compound) {
                    s = s[..s.len() - compound.len()].to_string();
                    return s;
                }
            }
            // Then strip single extensions
            for ext in &[
                ".gz", ".bam", ".fastq", ".fa", ".fq", ".bed", ".vcf", ".sam", ".cram",
            ] {
                if s.ends_with(ext) {
                    s = s[..s.len() - ext.len()].to_string();
                    break;
                }
            }
            s
        };
        let input_base = strip_extensions(&input_name);
        let output_base = strip_extensions(&output_name);

        // Apply template with single-pass replacement to avoid intermediate Strings
        let args =
            if rule.args_template.contains("{input}") || rule.args_template.contains("{output}") {
                let mut result = String::with_capacity(
                    rule.args_template.len() + input_base.len() + output_base.len(),
                );
                let mut i = 0;
                let template = rule.args_template.as_str();
                while i < template.len() {
                    if template[i..].starts_with("{input}") {
                        result.push_str(&input_base);
                        i += 7;
                    } else if template[i..].starts_with("{output}") {
                        result.push_str(&output_base);
                        i += 8;
                    } else {
                        result.push(template[i..].chars().next().unwrap());
                        i += template[i..].chars().next().unwrap().len_utf8();
                    }
                }
                result
            } else {
                rule.args_template.clone()
            };

        GeneratedCommand {
            args,
            explanation: rule.explanation_template.clone(),
            confidence: rule.confidence,
            source: CommandSource::Rule {
                rule_id: rule.id.clone(),
            },
        }
    }
}

impl Default for RuleCommandGenerator {
    fn default() -> Self {
        Self::new()
    }
}

#[async_trait]
impl CommandGenerator for RuleCommandGenerator {
    async fn generate(
        &self,
        tool: &str,
        _docs: &str,
        _skill: Option<&Skill>,
        user_request: &str,
        _config: &Config,
    ) -> Result<GeneratedCommand> {
        // Find matching rule
        for rule in &self.rules {
            if self.matches(rule, tool, user_request) {
                return Ok(self.apply(rule, user_request));
            }
        }

        // No matching rule
        Err(crate::error::OxoError::LlmError(
            "No matching rule for this request".to_string(),
        ))
    }

    fn name(&self) -> &str {
        "rule"
    }
}

