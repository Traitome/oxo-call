//! Command Generator Trait and Implementations
//!
//! This module provides an abstraction layer for command generation,
//! decoupling the generation logic from LLM clients and enabling
//! multiple generation strategies (LLM, rules, templates).
//!
//! # Architecture
//!
//! ```text
//! CommandGenerator (trait)
//!     ├── LlmCommandGenerator (uses LlmClient)
//!     ├── RuleCommandGenerator (pattern matching)
//!     └── CompositeGenerator (chain of responsibility)
//! ```
//!
//! # Usage
//!
//! ```rust,ignore
//! let generator = CompositeGenerator::new(vec![
//!     Box::new(RuleCommandGenerator::new()),
//!     Box::new(LlmCommandGenerator::new(client)),
//! ]);
//! let cmd = generator.generate(tool, docs, skill, request, config).await?;
//! ```

use crate::config::Config;
