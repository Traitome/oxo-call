// CommandGenerator Trait
// ────────────────────────────────────────────────────────────────────────────

/// Trait for command generation strategies.
///
/// Implementations can use different approaches:
/// - LLM-based generation (primary)
/// - Rule-based matching (fast path)
/// - Template instantiation (standard workflows)
#[async_trait]
#[allow(dead_code)]
pub trait CommandGenerator: Send + Sync {
    /// Generate a command from the given context.
    ///
    /// # Arguments
    ///
    /// * `tool` - Tool name (e.g., "samtools", "bwa")
    /// * `docs` - Tool documentation (from `--help` or cached)
    /// * `skill` - Optional skill file with examples
    /// * `user_request` - Natural language description of the task
    /// * `config` - Global configuration
    ///
    /// # Returns
    ///
    /// Returns a `GeneratedCommand` with args, explanation, confidence, and source.
    async fn generate(
        &self,
        tool: &str,
        docs: &str,
        skill: Option<&Skill>,
        user_request: &str,
        config: &Config,
    ) -> Result<GeneratedCommand>;

    /// Returns the name of this generator (for logging and debugging).
    fn name(&self) -> &str;
}

