/// Composite generator that tries multiple generators in order.
///
/// This implements the chain of responsibility pattern, trying each
/// generator in sequence until one succeeds.
#[allow(dead_code)]
pub struct CompositeGenerator {
    generators: Vec<Box<dyn CommandGenerator>>,
}

#[allow(dead_code)]
impl CompositeGenerator {
    /// Creates a new composite generator with the given generators.
    pub fn new(generators: Vec<Box<dyn CommandGenerator>>) -> Self {
        Self { generators }
    }

    /// Creates a default composite with rule → LLM fallback.
    pub fn default_composite(client: LlmClient) -> Self {
        Self::new(vec![
            Box::new(RuleCommandGenerator::new()),
            Box::new(LlmCommandGenerator::new(client)),
        ])
    }
}

#[async_trait]
impl CommandGenerator for CompositeGenerator {
    async fn generate(
        &self,
        tool: &str,
        docs: &str,
        skill: Option<&Skill>,
        user_request: &str,
        config: &Config,
    ) -> Result<GeneratedCommand> {
        let mut last_error = None;

        for generator in &self.generators {
            match generator
                .generate(tool, docs, skill, user_request, config)
                .await
            {
                Ok(cmd) => {
                    tracing::debug!("Command generated by {}: {}", generator.name(), cmd.args);
                    return Ok(cmd);
                }
                Err(e) => {
                    tracing::trace!("Generator {} failed: {}, trying next", generator.name(), e);
                    last_error = Some(e);
                }
            }
        }

        // All generators failed
        Err(last_error.unwrap_or_else(|| {
            crate::error::OxoError::LlmError("All generators failed".to_string())
        }))
    }

    fn name(&self) -> &str {
        "composite"
    }
}

/// Check if haystack contains needle case-insensitively without allocation.
fn contains_ignore_case(haystack: &str, needle: &str) -> bool {
    if needle.is_empty() {
        return true;
    }
    if haystack.len() < needle.len() {
        return false;
    }

    let needle_chars: Vec<char> = needle.chars().collect();
    let mut haystack_iter = haystack.chars().peekable();

    while let Some(first) = haystack_iter.next() {
        if first.eq_ignore_ascii_case(&needle_chars[0]) {
            let mut matched = true;
            for needle_char in &needle_chars[1..] {
                match haystack_iter.peek() {
                    Some(h_char) if h_char.eq_ignore_ascii_case(needle_char) => {
                        haystack_iter.next();
                        continue;
                    }
                    _ => {
                        matched = false;
                        break;
                    }
                }
            }
            if matched {
                return true;
            }
        }
    }
    false
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_rule_generator_matches() {
        let generator = RuleCommandGenerator::new();

        // Should match
        assert!(generator.matches(
            &generator.rules[0],
            "samtools",
            "sort bam file by coordinate"
        ));

        // Should not match (wrong tool)
        assert!(!generator.matches(&generator.rules[0], "fastqc", "sort bam file"));

        // Should not match (wrong request)
        assert!(!generator.matches(&generator.rules[0], "samtools", "index bam file"));
    }

    #[test]
    fn test_rule_generator_apply() {
        let generator = RuleCommandGenerator::new();
        let cmd = generator.apply(&generator.rules[0], "sort bam file");

        assert_eq!(cmd.args, "sort -o output.bam input.bam");
        assert_eq!(cmd.explanation, "Sort BAM file by genomic coordinates");
        assert!(cmd.confidence > 0.9);
    }

    #[test]
    fn test_command_source_name() {
        let llm_source = CommandSource::Llm {
            model: "gpt-4".to_string(),
            cache_hit: false,
        };
        assert_eq!(llm_source.name(), "llm");

        let rule_source = CommandSource::Rule {
            rule_id: "test".to_string(),
        };
        assert_eq!(rule_source.name(), "rule");
    }
}
