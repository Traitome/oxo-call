/// Command generation result with metadata.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[allow(dead_code)]
pub struct GeneratedCommand {
    /// The command arguments (e.g., "sort -@ 8 -o sorted.bam input.bam")
    pub args: String,
    /// Human-readable explanation of what the command does
    pub explanation: String,
    /// Confidence score (0.0 = uncertain, 1.0 = highly confident)
    pub confidence: f32,
    /// Source of the generated command
    pub source: CommandSource,
}

/// Source of a generated command.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[allow(dead_code)]
pub enum CommandSource {
    /// Generated by LLM API
    Llm { model: String, cache_hit: bool },
    /// Generated by rule matching
    Rule { rule_id: String },
    /// Generated from a template
    Template { template_id: String },
}

#[allow(dead_code)]
impl CommandSource {
    /// Returns a human-readable name of the source.
    pub fn name(&self) -> &str {
        match self {
            CommandSource::Llm { .. } => "llm",
            CommandSource::Rule { .. } => "rule",
            CommandSource::Template { .. } => "template",
        }
    }
}

